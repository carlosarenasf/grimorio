/**
 * Build a spell slot table for full casters (1-20 → 1-9 slots).
 * The shape is a tuple [L1, L2, ..., L9]; 0 means no slot.
 */
function spellTable(rows: [number, number, number, number, number, number, number, number, number][]): Record<number, [number, number, number, number, number, number, number, number, number]> {
  return Object.fromEntries(
    rows.map((row, i) => [i + 1, row]),
  ) as Record<number, [number, number, number, number, number, number, number, number, number]>;
}

